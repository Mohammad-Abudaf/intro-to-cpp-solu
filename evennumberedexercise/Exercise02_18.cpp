#include <iostream>
#include <cmath>
using namespace std;

int main()
{
  cout << "a      b      pow(a, b)" << endl;
  cout << "1      2      " << (int)pow(1.0, 2) << endl;
  cout << "2      3      " << (int)pow(2.0, 3) << endl;
  cout << "3      4      " << (int)pow(3.0, 4) << endl;
  cout << "4      5      " << (int)pow(4.0, 5) << endl;
  cout << "5      6      " << (int)pow(5.0, 6) << endl;

  return 0;
}
